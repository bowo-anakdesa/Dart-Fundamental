main() {
  // Equal; see discussion below
  assert(2 == 2);
  print(2 == 2);

  // Not equal
  assert(2 != 3);
  print(2 != 3);

  // Greater than
  assert(3 > 2);
  print(3 > 2);

  // Less than
  assert(2 < 3);
  print(2 < 3);

  // Greater than or equal to
  assert(3 >= 3);
  print(3 >= 3);

  // Less than or equal to
  assert(2 <= 3);
  print(2 <= 3);
}
